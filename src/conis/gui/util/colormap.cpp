/**
 * @brief ColorMap::ColorMap Creates a new color map object containing various
 * different color maps.
 */
ColorMap::ColorMap() {
    colorMaps[ColorMapName::TURBO] = getTurboRainbowColorMap();
    colorMaps[ColorMapName::PLASMA] = getPlasmaColorMap();
    colorMaps[ColorMapName::BLACK_BODY] = getBlackBodyColorMap();
    colorMaps[ColorMapName::INFERNO] = getInfernoColorMap();
    colorMaps[ColorMapName::KINDLMANN] = getKindlmannColorMap();
    colorMaps[ColorMapName::SMOOTH_COOL_WARM] = getSmoothCoolWarmColorMap();
    colorMaps[ColorMapName::MPL_COOL] = getMPLCoolColorMap();
    colorMaps[ColorMapName::OCEAN_DEEP] = getOceanDeepColorMap();
    colorMaps[ColorMapName::CMP_HAXBY] = getCMPHaxbyColorMap();
}

/**
 * @brief ColorMap::getColorMap Returns the color values of the color map with
 * the provided name.
 * @param name The name of the color map.
 * @return A collection containing (r,g,b) triples .
 */
std::vector<QVector3D> &ColorMap::getColorMap(ColorMapName name) {
    return colorMaps[name];
}

